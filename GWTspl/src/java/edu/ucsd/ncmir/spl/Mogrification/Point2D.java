package edu.ucsd.ncmir.spl.Mogrification;

/**
 *
 * @author spl
 */
public class Point2D

{

    private double _x;
    private double _y;

    public Point2D( double x, double y )

    {

        this._x = x;
        this._y = y;

    }

    public double getX()

    {

        return this._x;

    }

    public double getY()

    {

        return this._y;

    }

}
